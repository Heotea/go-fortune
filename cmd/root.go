package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "go-fortune",
	Short: "Go Fortune Teller CLI",
	Long:  `Go Fortune Teller CLI`,
	Run:   func(cmd *cobra.Command, args []string) { fmt.Println("Go Furtune Teller CLI") },
}

func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

func init() {
	rootCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
}
